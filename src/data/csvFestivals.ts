/** Derive a slug from a festival name the same way the CSV data uses */
export function toSlug(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

/** Look up a CsvFestival by slug */
export function getCsvFestivalBySlug(slug: string): CsvFestival | undefined {
  return csvFestivals.find((f) => f.slug === slug);
}
