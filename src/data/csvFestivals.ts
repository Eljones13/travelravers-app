// TRAVEL RAVERS: CSV-derived festival data
// Merged from travel_ravers_festivals.csv + travel_ravers_youtube.csv
// lat/lng are best-effort coordinates for the festival venue location.
// Used for Booking.com affiliate links and the fallback detail page.

export interface CsvFestival {
  slug: string;
  festivalName: string;
  imageUrl: string;
  imageSource: string;
  headliners: string | "TBA";
  lineupSourceUrl: string;
  ticketUrl: string;
  skiddleTicketUrl: string | "NO_SKIDDLE";
  youtubeVideoId: string | "NO_VIDEO";
  // Location context (from content.ts or manually added)
  dates: string;
  location: string;
  country: string;
  genre: string;
  camping: boolean;
  lat: number;
  lng: number;
  startDate: string; // ISO YYYY-MM-DD
  endDate: string;   // ISO YYYY-MM-DD
  // Extended fields for affiliate revenue + travel intel
  nearestAirport?: string;      // IATA code e.g. "MAN"
  nearestCity?: string;         // City name for Stay22 / Skyscanner
  mudrisk?: 0 | 1;             // 1 = high mud risk (UK camping)
  heatrisk?: 0 | 1;            // 1 = high heat risk (Mediterranean summer)
  capacity?: number;            // approximate attendee capacity
  stages?: number;              // number of stages
  website?: string;             // official festival website
  officialTicketUrl?: string;   // direct ticket URL if not Skiddle
  spotifyPlaylist?: string;     // Spotify playlist embed ID
  youtubeSearchQuery?: string;  // query for YouTube search card
  affiliateNote?: string;       // internal note about affiliate status
}

